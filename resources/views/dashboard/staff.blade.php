<h1>Dokumentasi Halaman Staff</h1>

    <div class="section">
        <h2>Deskripsi Umum</h2>
        <p>Halaman <strong>Staff</strong> adalah antarmuka untuk mengelola informasi staf dalam aplikasi Buku Induk Nilai SMK 4 Garut. Halaman ini menyediakan fitur untuk menambahkan, melihat, dan mengedit informasi staf, serta melakukan reset password bagi pengguna yang terdaftar.</p>
    </div>

    <div class="section">
        <h2>Fitur Utama</h2>
        <ul>
            <li><strong>Menampilkan Notifikasi Kesalahan</strong>: Halaman ini menampilkan pesan kesalahan jika terdapat input yang tidak valid.</li>
            <li><strong>Notifikasi Sukses</strong>: Menampilkan pesan sukses ketika suatu tindakan berhasil, seperti menambah atau mengubah informasi staf.</li>
            <li><strong>Tombol Tambah Staff</strong>: Admin dapat menambah staf baru dengan mengklik tombol "Tambah Staff".</li>
            <li><strong>Daftar Staf</strong>: Tabel yang menampilkan daftar semua staf beserta nama, email, dan peran mereka.</li>
            <li><strong>Status Peran Staf</strong>: Menampilkan peran staf dengan badge yang berwarna, seperti Admin atau Staff TU Kesiswaan.</li>
            <li><strong>Tindakan untuk Setiap Staf</strong>: Terdapat tombol untuk melihat, mengedit, dan mereset password staf.</li>
        </ul>
    </div>
