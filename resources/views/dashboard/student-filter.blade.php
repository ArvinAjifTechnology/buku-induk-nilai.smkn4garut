<h1>Dokumentasi Halaman Filter Data Siswa</h1>

<div class="section">
    <h2>Deskripsi Halaman</h2>
    <p>Halaman <strong>Data Siswa</strong> digunakan oleh admin untuk mengelola dan melihat data lengkap mengenai siswa. Halaman ini menyediakan fitur untuk memfilter, mengimpor, mengekspor, dan melihat detail siswa secara lengkap.</p>
</div>

<div class="section">
    <h2>Fitur Utama</h2>
    <ul>
        <li><strong>Import dan Export Data Siswa</strong>: Admin dapat mengimpor data siswa dari file Excel dan mengekspor data siswa ke file Excel menggunakan tombol yang tersedia.</li>
        <li><strong>Filter Data Siswa</strong>: Admin dapat memfilter data siswa berdasarkan beberapa kriteria seperti Tahun Masuk, Jurusan, Kelas, dan Status Siswa.</li>
        <li><strong>Daftar Siswa</strong>: Menampilkan tabel data lengkap siswa yang mencakup informasi pribadi seperti NISN, Nama, Kelas, Jurusan, Gender, Alamat, dan data orang tua.</li>
        <li><strong>Detail, Edit, dan Hapus</strong>: Admin dapat melihat detail siswa, mengedit data siswa, atau menghapus data siswa yang sudah tidak diperlukan.</li>
    </ul>
</div>

<div class="section">
    <h2>Komponen Halaman</h2>
    <ul>
        <li><strong>Notifikasi</strong>: Terdapat notifikasi pesan kesalahan jika ada masalah dengan input, dan pesan sukses jika operasi berhasil dilakukan.</li>
        <li><strong>Form Import</strong>: Form untuk mengunggah file Excel yang berisi data siswa yang akan diimpor.</li>
        <li><strong>Form Filter Data</strong>: Admin dapat menyaring data siswa dengan memilih opsi Tahun Masuk, Jurusan, Kelas, dan Status Siswa. Filter ini akan membantu admin mencari siswa sesuai kriteria tertentu.</li>
        <li><strong>Tabel Data Siswa</strong>: Tabel ini menampilkan data siswa secara lengkap, termasuk informasi pribadi dan akademik. Setiap baris memiliki aksi untuk melihat detail, mengedit, atau menghapus data siswa.</li>
    </ul>
</div>
