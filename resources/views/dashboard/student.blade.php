<h1>Dokumentasi Halaman Data Siswa</h1>

    <div class="section">
        <h2>Deskripsi Umum</h2>
        <p>Halaman <strong>Data Siswa</strong> adalah antarmuka untuk mengelola informasi siswa dalam aplikasi Buku Induk Nilai SMK 4 Garut. Halaman ini dirancang untuk memberikan kemudahan dalam menambah, mengedit, menghapus, dan mengelola data siswa, serta melakukan upload foto dan impor data siswa dari file Excel.</p>
    </div>

    <div class="section">
        <h2>Fitur Utama</h2>
        <ul>
            <li><strong>Tombol Tambah</strong>: Admin dapat menambah siswa baru dengan mengklik tombol "Tambah".</li>
            <li><strong>Form Upload Foto</strong>: Admin dapat mengunggah foto siswa dalam format gambar dan nama fike sesuai <strong>NIS</strong> dengan tombol "Upload Foto".</li>
            <li><strong>Download Template</strong>: Admin dapat mendownload template file Excel untuk memasukkan data siswa dengan lebih mudah.</li>
            <li><strong>Form Import</strong>: Memungkinkan admin untuk mengimpor data siswa dari file Excel dengan memilih file dan mengklik tombol "Import".</li>
            <li><strong>Filter Data</strong>: Admin dapat memfilter data siswa berdasarkan tahun masuk, jurusan, kelas, dan status siswa, untuk memudahkan pencarian data.</li>
            <li><strong>Pencarian Data Siswa</strong>: Menyediakan kotak pencarian untuk mencari siswa berdasarkan nama, NISN, atau NIK.</li>
            <li><strong>Daftar Siswa</strong>: Menampilkan tabel yang berisi daftar siswa, termasuk informasi penting seperti tahun masuk, jurusan, kelas, NIS, dan nama lengkap.</li>
            <li><strong>Tindakan untuk Setiap Siswa</strong>: Terdapat tombol untuk melihat detail, mengedit, dan menghapus data siswa dengan konfirmasi.</li>
            <li><strong>Pagination</strong>: Menggunakan pagination untuk navigasi yang lebih baik pada daftar siswa yang panjang.</li>
        </ul>
    </div>
