<h1>Dokumentasi Halaman Detail Data Siswa</h1>

<div class="section">
    <h2>Deskripsi Halaman</h2>
    <p>Halaman <strong>Detail Data Siswa</strong> digunakan oleh admin untuk mengelola dan melihat data lengkap mengenai salah satu siswa. Halaman ini menyediakan fitur untuk  mengimpor, mengekspor, dan melihat detail siswa secara lengkap.</p>
</div>

<div class="section">
    <h2>Fitur Utama</h2>
    <ul>
        <li><strong>Detail Siswa</strong>: Menampilkan tabel data lengkap siswa yang mencakup informasi pribadi seperti NISN, Nama, Kelas, Jurusan, Gender, Alamat, dan data orang tua.</li>
        <li><strong>Import dan Export Data Siswa</strong>: Admin dapat mengimpor data siswa dari file Excel dan mengekspor data siswa ke file Excel menggunakan tombol yang tersedia.</li>
        <li><strong>Detail, Edit, dan Hapus</strong>: Admin dapat melihat detail siswa, mengedit data siswa, atau menghapus data siswa yang sudah tidak diperlukan.</li>
    </ul>
</div>

<div class="section">
    <h2>Komponen Halaman</h2>
    <ul>
        <li><strong>Notifikasi</strong>: Terdapat notifikasi pesan kesalahan jika ada masalah dengan input, dan pesan sukses jika operasi berhasil dilakukan.</li>
        <li><strong>Detail Siswa </strong>: Menampilkan Data Siswa Secara Detail</li>
        <li><strong>Export Nilai</strong>: Tersedia 2 Tombol Yaitu Export Nilai Siswa berupa Excel dan Berupa Word</li>
        <li><strong>Form Import</strong>: Form untuk mengunggah file Excel yang berisi data nilai siswa yang akan diimpor.</li>
        <li><strong>Tabel Data Nilai nSiswa</strong>: Tabel ini menampilkan data nilai siswa dari semetr 1-6 dan tersedia pehitungan jumlah nilai dan Pencapaian Siswa.</li>
    </ul>
</div>
